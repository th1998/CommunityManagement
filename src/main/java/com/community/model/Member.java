package com.community.model;

public class Member {
    private Integer v_id;
    private Integer co_id;
    private Integer u_id;
    private String v_name;
    private String v_class;
    private String v_dept;
    private String v_no;
    private String v_tel;

    public Integer getV_id() {
        return v_id;
    }

    public void setV_id(Integer v_id) {
        this.v_id = v_id;
    }

    public Integer getCo_id() {
        return co_id;
    }

    public void setCo_id(Integer co_id) {
        this.co_id = co_id;
    }

    public Integer getU_id() {
        return u_id;
    }

    public void setU_id(Integer u_id) {
        this.u_id = u_id;
    }

    public String getV_name() {
        return v_name;
    }

    public void setV_name(String v_name) {
        this.v_name = v_name;
    }

    public String getV_class() {
        return v_class;
    }

    public void setV_class(String v_class) {
        this.v_class = v_class;
    }

    public String getV_dept() {
        return v_dept;
    }

    public void setV_dept(String v_dept) {
        this.v_dept = v_dept;
    }

    public String getV_no() {
        return v_no;
    }

    public void setV_no(String v_no) {
        this.v_no = v_no;
    }

    public String getV_tel() {
        return v_tel;
    }

    public void setV_tel(String v_tel) {
        this.v_tel = v_tel;
    }
}